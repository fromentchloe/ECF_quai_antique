      <h3 class="left-to-right title p-5" >Le Restaurant  </h3>
      <p class="about_text">Pour son troisième restaurant de burger, le chef Michant a choisi de s'implanter au cœur de Chambéry, 
      dans un quartier animé et dynamique de la ville. Il a voulu créer un lieu convivial où les clients 
      pourraient se sentir à l'aise et profiter d'une expérience culinaire exceptionnelle.</p>
      <p>Le restaurant de burger du Chef Michaud à Chambéry offre une variété de burgers savoureux et uniques, élaborés à partir 
      d'ingrédients frais et locaux. Chaque burger est préparé à la commande pour garantir sa fraîcheur et sa qualité.</p>
      <p>En plus des burgers classiques, le menu propose également des options végétariennes, ainsi que des accompagnements tels que des frites maison et des salades fraîches. Le chef Michaud 
      accorde une grande importance à la qualité de ses ingrédients et s'assure que chaque plat est parfaitement 
      équilibré en termes de saveurs et de textures.</p>
      <p>Le restaurant est décoré dans un style industriel moderne avec des touches de couleurs chaleureuses. Les clients peuvent choisir de s'installer au bar ou de prendre 
      une table pour profiter d'une expérience culinaire décontractée et conviviale.</p>
      <p>Le chef Michaud est fier de proposer une carte de vins et de bières locales pour accompagner les burgers et les plats proposés. 
      Les clients peuvent également profiter d'une sélection de desserts faits maison, notamment la tarte aux myrtilles, 
      le fondant ou notre désormais célèbre croûte aux pommes fraîches.</p>
      <p>En somme, le restaurant de burger du Chef Michaud à Chambéry est un endroit incontournable pour tous 
      les amateurs de burgers qui cherchent une expérience culinaire de qualité dans une atmosphère chaleureuse 
      et conviviale.</p>
