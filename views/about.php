<h4 class="about_title">A propos ...</h4>

<p>Pour son troisième restaurant de burger, le chef Michaud a choisi de s'implanter au cœur de Chambéry, 
dans un quartier animé et dynamique de la ville. Il a voulu créer un lieu convivial où les clients 
pourraient se sentir à l'aise et profiter d'une expérience culinaire exceptionnelle. Le restaurant de 
burger de Chef Michaud à Chambéry offre une variété de burgers savoureux et uniques, élaborés à partir 
d'ingrédients frais et locaux Chaque burger est préparé à la commande pour garantir sa fraîcheur et sa 
qualité.
En plus des burgers classiques, le menu propose également une options végétariennes, ainsi que 
des accompagnements tels que des frites maison, et des salades fraîches. Le chef Michaud 
accorde une grande importance à la qualité de ses ingrédients et s'assure que chaque plat est parfaitement 
équilibré en termes de saveurs et de textures.Le restaurant est décoré dans un style industriel moderne 
avec des touches au couleurs chaleureuses. Les clients peuvent choisir de s'installer au bar ou de prendre 
une table pour profiter d'une expérience culinaire décontractée et conviviale.Le chef Michaud est fier 
de proposer <span>une carte de vins </span>et de <span>ères locales</span> pour accompagner <span>les burgerset les plats proposés. </span> 
Les clients peuvent également profiter d'une sélection de <span>desserts faits maison</span>, notamment la tartes aux myrtilles, 
le Fondant ou bien notre désormais célèbre croûte au pommes fraîches.
En somme, le restaurant de burger de Chef Michaud à Chambéry est un endroit incontournable pour tous 
les amateurs de burgers qui cherchent une expérience culinaire de qualité dans une atmosphère chaleureuse 
et conviviale.</p>